use std::collections::HashMap;

/// ローマ字→ひらがな逆引きテーブルを構築する（n-gram スコアリング用）。
///
/// 訓令式ローマ字を基本とし、ヘボン式の一部（"fu"→'ふ' 等）も含む。
/// 拗音（"kya" 等）は n-gram のバイグラム文脈で先頭文字が重要なため、
/// 代表文字（'き' 等）にマッピングする。
#[allow(clippy::too_many_lines)]
#[must_use]
pub fn build_romaji_to_kana() -> HashMap<String, char> {
    let entries: &[(&str, char)] = &[
        // 母音
        ("a", 'あ'),
        ("i", 'い'),
        ("u", 'う'),
        ("e", 'え'),
        ("o", 'お'),
        // カ行
        ("ka", 'か'),
        ("ki", 'き'),
        ("ku", 'く'),
        ("ke", 'け'),
        ("ko", 'こ'),
        // サ行
        ("sa", 'さ'),
        ("si", 'し'),
        ("su", 'す'),
        ("se", 'せ'),
        ("so", 'そ'),
        // タ行
        ("ta", 'た'),
        ("ti", 'ち'),
        ("tu", 'つ'),
        ("te", 'て'),
        ("to", 'と'),
        // ナ行
        ("na", 'な'),
        ("ni", 'に'),
        ("nu", 'ぬ'),
        ("ne", 'ね'),
        ("no", 'の'),
        // ハ行
        ("ha", 'は'),
        ("hi", 'ひ'),
        ("hu", 'ふ'),
        ("fu", 'ふ'),
        ("he", 'へ'),
        ("ho", 'ほ'),
        // マ行
        ("ma", 'ま'),
        ("mi", 'み'),
        ("mu", 'む'),
        ("me", 'め'),
        ("mo", 'も'),
        // ヤ行
        ("ya", 'や'),
        ("yu", 'ゆ'),
        ("yo", 'よ'),
        // ラ行
        ("ra", 'ら'),
        ("ri", 'り'),
        ("ru", 'る'),
        ("re", 'れ'),
        ("ro", 'ろ'),
        // ワ行
        ("wa", 'わ'),
        ("wo", 'を'),
        // ン
        ("nn", 'ん'),
        // 濁音
        ("ga", 'が'),
        ("gi", 'ぎ'),
        ("gu", 'ぐ'),
        ("ge", 'げ'),
        ("go", 'ご'),
        ("za", 'ざ'),
        ("zi", 'じ'),
        ("zu", 'ず'),
        ("ze", 'ぜ'),
        ("zo", 'ぞ'),
        ("da", 'だ'),
        ("di", 'ぢ'),
        ("du", 'づ'),
        ("de", 'で'),
        ("do", 'ど'),
        ("ba", 'ば'),
        ("bi", 'び'),
        ("bu", 'ぶ'),
        ("be", 'べ'),
        ("bo", 'ぼ'),
        // 半濁音
        ("pa", 'ぱ'),
        ("pi", 'ぴ'),
        ("pu", 'ぷ'),
        ("pe", 'ぺ'),
        ("po", 'ぽ'),
        // 拗音（先頭文字で代表）
        ("kya", 'き'),
        ("kyu", 'き'),
        ("kyo", 'き'),
        ("sya", 'し'),
        ("syu", 'し'),
        ("syo", 'し'),
        ("tya", 'ち'),
        ("tyu", 'ち'),
        ("tyo", 'ち'),
        ("nya", 'に'),
        ("nyu", 'に'),
        ("nyo", 'に'),
        ("hya", 'ひ'),
        ("hyu", 'ひ'),
        ("hyo", 'ひ'),
        ("mya", 'み'),
        ("myu", 'み'),
        ("myo", 'み'),
        ("rya", 'り'),
        ("ryu", 'り'),
        ("ryo", 'り'),
        ("gya", 'ぎ'),
        ("gyu", 'ぎ'),
        ("gyo", 'ぎ'),
        ("zya", 'じ'),
        ("zyu", 'じ'),
        ("zyo", 'じ'),
        ("dya", 'ぢ'),
        ("dyu", 'ぢ'),
        ("dyo", 'ぢ'),
        ("bya", 'び'),
        ("byu", 'び'),
        ("byo", 'び'),
        ("pya", 'ぴ'),
        ("pyu", 'ぴ'),
        ("pyo", 'ぴ'),
        // 小書き
        ("la", 'ぁ'),
        ("li", 'ぃ'),
        ("lu", 'ぅ'),
        ("le", 'ぇ'),
        ("lo", 'ぉ'),
        ("lya", 'ゃ'),
        ("lyu", 'ゅ'),
        ("lyo", 'ょ'),
        ("ltu", 'っ'),
        // 特殊
        ("vu", 'ゔ'),
    ];
    entries.iter().map(|&(k, v)| (k.to_string(), v)).collect()
}
