//! Windows Set 1 スキャンコード ⇔ PhysicalPos マッピング

use awase::scanmap::PhysicalPos;
use awase::types::ScanCode;

/// Windows Set 1 スキャンコード → JIS キーボード物理位置
#[must_use]
pub const fn scan_to_pos(scan_code: ScanCode) -> Option<PhysicalPos> {
    let (row, col) = match scan_code.0 {
        0x02 => (0, 0),
        0x03 => (0, 1),
        0x04 => (0, 2),
        0x05 => (0, 3),
        0x06 => (0, 4),
        0x07 => (0, 5),
        0x08 => (0, 6),
        0x09 => (0, 7),
        0x0A => (0, 8),
        0x0B => (0, 9),
        0x0C => (0, 10),
        0x0D => (0, 11),
        0x7D => (0, 12),
        0x10 => (1, 0),
        0x11 => (1, 1),
        0x12 => (1, 2),
        0x13 => (1, 3),
        0x14 => (1, 4),
        0x15 => (1, 5),
        0x16 => (1, 6),
        0x17 => (1, 7),
        0x18 => (1, 8),
        0x19 => (1, 9),
        0x1A => (1, 10),
        0x1B => (1, 11),
        0x1E => (2, 0),
        0x1F => (2, 1),
        0x20 => (2, 2),
        0x21 => (2, 3),
        0x22 => (2, 4),
        0x23 => (2, 5),
        0x24 => (2, 6),
        0x25 => (2, 7),
        0x26 => (2, 8),
        0x27 => (2, 9),
        0x28 => (2, 10),
        0x2B => (2, 11),
        0x2C => (3, 0),
        0x2D => (3, 1),
        0x2E => (3, 2),
        0x2F => (3, 3),
        0x30 => (3, 4),
        0x31 => (3, 5),
        0x32 => (3, 6),
        0x33 => (3, 7),
        0x34 => (3, 8),
        0x35 => (3, 9),
        0x73 => (3, 10),
        _ => return None,
    };
    Some(PhysicalPos::new(row, col))
}

/// JIS キーボード物理位置 → Windows Set 1 スキャンコード
#[must_use]
pub const fn pos_to_scan(pos: PhysicalPos) -> Option<ScanCode> {
    let raw = match (pos.row, pos.col) {
        (0, 0) => 0x02,
        (0, 1) => 0x03,
        (0, 2) => 0x04,
        (0, 3) => 0x05,
        (0, 4) => 0x06,
        (0, 5) => 0x07,
        (0, 6) => 0x08,
        (0, 7) => 0x09,
        (0, 8) => 0x0A,
        (0, 9) => 0x0B,
        (0, 10) => 0x0C,
        (0, 11) => 0x0D,
        (0, 12) => 0x7D,
        (1, 0) => 0x10,
        (1, 1) => 0x11,
        (1, 2) => 0x12,
        (1, 3) => 0x13,
        (1, 4) => 0x14,
        (1, 5) => 0x15,
        (1, 6) => 0x16,
        (1, 7) => 0x17,
        (1, 8) => 0x18,
        (1, 9) => 0x19,
        (1, 10) => 0x1A,
        (1, 11) => 0x1B,
        (2, 0) => 0x1E,
        (2, 1) => 0x1F,
        (2, 2) => 0x20,
        (2, 3) => 0x21,
        (2, 4) => 0x22,
        (2, 5) => 0x23,
        (2, 6) => 0x24,
        (2, 7) => 0x25,
        (2, 8) => 0x26,
        (2, 9) => 0x27,
        (2, 10) => 0x28,
        (2, 11) => 0x2B,
        (3, 0) => 0x2C,
        (3, 1) => 0x2D,
        (3, 2) => 0x2E,
        (3, 3) => 0x2F,
        (3, 4) => 0x30,
        (3, 5) => 0x31,
        (3, 6) => 0x32,
        (3, 7) => 0x33,
        (3, 8) => 0x34,
        (3, 9) => 0x35,
        (3, 10) => 0x73,
        _ => return None,
    };
    Some(ScanCode(raw))
}
